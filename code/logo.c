void load_logo(void)
{
	for (int y = 0; y < 8; y++)
	{
		glcd_command(GLCD_CMD_SET_PAGE | y);
		glcd_command(GLCD_CMD_COLUMN_LOWER | 4);
		glcd_command(GLCD_CMD_COLUMN_LOWER);
		glcd_command(GLCD_CMD_COLUMN_UPPER);

		for (int x = 0; x < 128; x++)
		{
			glcd_data(logo_sleep[y * 128 + x]);
		}
	}
}

